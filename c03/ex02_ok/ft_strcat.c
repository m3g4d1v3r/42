char	*ft_strcat(char *dest, char *src)
{
	char	*dest_init;

	dest_init = dest;
	while (*dest)
		dest++;
	while (*src)
	{
		*dest = *src;
		src++;
		dest++;
	}
	*dest = '\0';
	return (dest_init);
}
